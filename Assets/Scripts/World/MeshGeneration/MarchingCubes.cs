	protected static void TriangulateCell(float isoLevel, in Voxel voxel0, in Voxel voxel1, in Voxel voxel2, in Voxel voxel3, in Voxel voxel4, in Voxel voxel5, in Voxel voxel6, in Voxel voxel7, in List<Vector3> vertices, 
		in List<int> triangles)
	{
		int cubeIndex = 0;
		Vector3[] verticesArray = new Vector3[12];

		if (voxel0.value < isoLevel) cubeIndex |= 1;
		if (voxel1.value < isoLevel) cubeIndex |= 2;
		if (voxel2.value < isoLevel) cubeIndex |= 4;
		if (voxel3.value < isoLevel) cubeIndex |= 8;
		if (voxel4.value < isoLevel) cubeIndex |= 16;
		if (voxel5.value < isoLevel) cubeIndex |= 32;
		if (voxel6.value < isoLevel) cubeIndex |= 64;
		if (voxel7.value < isoLevel) cubeIndex |= 128;

		if (edgeTable[cubeIndex] == 0) return;

		if ((edgeTable[cubeIndex] & 1) != 0) verticesArray[0] = InterpolateVertexPosition(voxel0, voxel1, isoLevel);
		if ((edgeTable[cubeIndex] & 2) != 0) verticesArray[1] = InterpolateVertexPosition(voxel1, voxel2, isoLevel);
		if ((edgeTable[cubeIndex] & 4) != 0) verticesArray[2] = InterpolateVertexPosition(voxel2, voxel3, isoLevel);
		if ((edgeTable[cubeIndex] & 8) != 0) verticesArray[3] = InterpolateVertexPosition(voxel3, voxel0, isoLevel);
		if ((edgeTable[cubeIndex] & 16) != 0) verticesArray[4] = InterpolateVertexPosition(voxel4, voxel5, isoLevel);
		if ((edgeTable[cubeIndex] & 32) != 0) verticesArray[5] = InterpolateVertexPosition(voxel5, voxel6, isoLevel);
		if ((edgeTable[cubeIndex] & 64) != 0) verticesArray[6] = InterpolateVertexPosition(voxel6, voxel7, isoLevel);
		if ((edgeTable[cubeIndex] & 128) != 0) verticesArray[7] = InterpolateVertexPosition(voxel7, voxel4, isoLevel);
		if ((edgeTable[cubeIndex] & 256) != 0) verticesArray[8] = InterpolateVertexPosition(voxel0, voxel4, isoLevel);
		if ((edgeTable[cubeIndex] & 512) != 0) verticesArray[9] = InterpolateVertexPosition(voxel1, voxel5, isoLevel);
		if ((edgeTable[cubeIndex] & 1024) != 0) verticesArray[10] = InterpolateVertexPosition(voxel2, voxel6, isoLevel);
		if ((edgeTable[cubeIndex] & 2048) != 0) verticesArray[11] = InterpolateVertexPosition(voxel3, voxel7, isoLevel);

		for (int i = 0; indexTable[cubeIndex, i] != -1; i += 3)
		{
		
			triangles.Add(vertices.Count);
			triangles.Add(vertices.Count + 1);
			triangles.Add(vertices.Count + 2);

			vertices.Add(verticesArray[indexTable[cubeIndex, i]]);
			vertices.Add(verticesArray[indexTable[cubeIndex, i + 1]]);
			vertices.Add(verticesArray[indexTable[cubeIndex, i + 2]]);
		}

	}

	protected static Vector3 InterpolateVertexPosition(in Voxel voxel0, in Voxel voxel1, float isoLevel)
	{
		Vector3 pos = Vector3.zero;

		if (Mathf.Abs(isoLevel - voxel0.value) < 0.00001)
			return voxel0.position;
		if (Mathf.Abs(isoLevel - voxel1.value) < 0.00001)
			return voxel1.position;
		if (Mathf.Abs(voxel0.value - voxel1.value) < 0.00001)
			return voxel0.position;
		float mu = (isoLevel - voxel0.value) / (voxel1.value - voxel0.value);
		pos.x = voxel0.position.x + mu * (voxel1.position.x - voxel0.position.x);
		pos.y = voxel0.position.y + mu * (voxel1.position.y - voxel0.position.y);
		pos.z = voxel0.position.z + mu * (voxel1.position.z - voxel0.position.z);

		return pos;
	}

